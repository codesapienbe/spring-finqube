/**
 * Payment Initiation (pain) message types for ISO 20022.
 *
 * <p>This package contains message types for the Payment Initiation business process,
 * which handles the initiation of various types of payments between parties.</p>
 *
 * <p>Key message types include:</p>
 * <ul>
 *   <li><strong>pain.001</strong> - Customer Credit Transfer Initiation</li>
 *   <li><strong>pain.002</strong> - Customer Payment Status Report</li>
 *   <li><strong>pain.003</strong> - Customer Direct Debit Initiation</li>
 *   <li><strong>pain.004</strong> - Customer Payment Reversal</li>
 *   <li><strong>pain.005</strong> - Customer Payment Cancellation Request</li>
 *   <li><strong>pain.006</strong> - Customer Payment Cancellation Status Report</li>
 *   <li><strong>pain.007</strong> - Customer Payment Modification Request</li>
 *   <li><strong>pain.008</strong> - Customer Payment Modification Status Report</li>
 *   <li><strong>pain.009</strong> - Customer Payment Return</li>
 *   <li><strong>pain.010</strong> - Customer Payment Reversal</li>
 *   <li><strong>pain.011</strong> - Mandate Initiation Request</li>
 *   <li><strong>pain.012</strong> - Mandate Amendment Request</li>
 *   <li><strong>pain.013</strong> - Mandate Cancellation Request</li>
 *   <li><strong>pain.014</strong> - Mandate Acceptance Report</li>
 * </ul>
 *
 * <p>The Payment Initiation process is used by customers to initiate payments
 * to their banks, which then process the payments through the appropriate
 * clearing and settlement systems.</p>
 *
 * @author Finqube Team
 * @version 0.1.0
 * @since 0.1.0
 */
